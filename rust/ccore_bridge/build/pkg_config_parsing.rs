pub fn emit_pkg_config_link_data(pkg_config_file: &std::path::Path) {

    // The pkg-config crate compares CARGO_CFG_TARGET_ARCH against the host
    // and refuses to probe when they differ (cross-compilation). Since we
    // control the .pc file — it was generated by our own CMake cross build
    // and already contains correct paths/flags for the target — we can
    // safely bypass this check.
    unsafe {
        std::env::set_var("PKG_CONFIG_PATH", pkg_config_file.display().to_string());
        std::env::set_var("PKG_CONFIG_ALLOW_CROSS", "1");
    }

    pkg_config::Config::new().statik(true).probe("ccore").expect("failed to probe ccore pkg-config file");
}